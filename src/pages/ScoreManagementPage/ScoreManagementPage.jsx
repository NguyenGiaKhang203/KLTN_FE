import React, { useEffect, useState } from "react";
import { Input, InputNumber, Select, Table, Modal, Button, Tooltip } from "antd";
import { EditOutlined, DeleteOutlined, PlusOutlined } from "@ant-design/icons";
import { useSelector } from "react-redux";
import * as ScoreService from "../../services/ScoreService";
import * as ClassService from "../../services/ClassService";
import * as ExamService from "../../services/ExamService";
import { PageHeader, FilterContainer, TableWrapper } from "./style";
import { toast } from "react-toastify";

const { Search } = Input;
const { Option } = Select;

const ScoreManagement = () => {
  const [classList, setClassList] = useState([]);
  const [examList, setExamList] = useState([]);
  const [selectedClass, setSelectedClass] = useState(null);
  const [selectedExam, setSelectedExam] = useState(null);
  const [searchText, setSearchText] = useState("");
  const [studentsInClass, setStudentsInClass] = useState([]);
  const [scoreList, setScoreList] = useState([]);
  const [isModalVisible, setIsModalVisible] = useState(false);
  const [isEditMode, setIsEditMode] = useState(false);
  const [editingScoreId, setEditingScoreId] = useState(null);

  const { user } = useSelector((state) => state.user);
  const userId = user?._id;
  const token = user?.access_token;

  useEffect(() => {
    const fetchClasses = async () => {
      try {
        const res = await ClassService.getClassbyTeacher(userId);
        setClassList(Array.isArray(res?.data) ? res.data : res);
      } catch (err) {
        console.error("L·ªói khi l·∫•y danh s√°ch l·ªõp:", err);
      }
    };
    if (token) fetchClasses();
  }, [token]);

  useEffect(() => {
    const fetchExams = async () => {
      if (selectedClass) {
        try {
          const res = await ExamService.getExamsByClassId(selectedClass, token);
          setExamList(Array.isArray(res?.data) ? res.data : res);
        } catch (err) {
          console.error("L·ªói khi l·∫•y danh s√°ch b√†i thi:", err);
          setExamList([]);
        }
      } else setExamList([]);
    };
    fetchExams();
  }, [selectedClass, token]);

  useEffect(() => {
    const fetchScores = async () => {
      if (selectedClass) {
        try {
          const res = await ScoreService.getAllScores(token);
          setScoreList(Array.isArray(res?.data) ? res.data : res);
        } catch (err) {
          console.error("L·ªói khi l·∫•y b·∫£ng ƒëi·ªÉm:", err);
        }
      }
    };
    fetchScores();
  }, [selectedClass, token]);

  const refreshScores = async () => {
    try {
      const res = await ScoreService.getAllScores(token);
      setScoreList(Array.isArray(res?.data) ? res.data : res);
    } catch (err) {
      console.error("L·ªói khi l√†m m·ªõi b·∫£ng ƒëi·ªÉm:", err);
    }
  };

  const checkScoreExists = (examId) => {
    return scoreList.some((score) => score.examId === examId);
  };

  const handleExamSelect = async (examId) => {
    setSelectedExam(examId);
    setIsEditMode(false);
    setEditingScoreId(null);
    try {
      const res = await ClassService.getStudentsInClass(selectedClass, token);
      setStudentsInClass(Array.isArray(res?.students) ? res.students : []);
      toast.success("ƒê√£ ch·ªçn b√†i thi, h√£y nh·∫≠p ƒëi·ªÉm cho h·ªçc vi√™n.");
      setIsModalVisible(true);
    } catch (err) {
      console.error("L·ªói khi l·∫•y danh s√°ch h·ªçc vi√™n:", err);
    }
  };

  const handleEditScore = async (examId) => {
    try {
      const res = await ScoreService.getScoreById(examId, token);
      const scores = res?.data?.scores || [];
      setEditingScoreId(res?.data?._id);
      setIsEditMode(true);

      const studentRes = await ClassService.getStudentsInClass(selectedClass, token);
      const studentsInThisClass = studentRes?.students || [];

      const mergedStudents = scores.map((scoreObj) => {
        const studentInfo = studentsInThisClass.find(
          (student) => student._id === scoreObj.studentId
        );
        return {
          _id: scoreObj.studentId,
          name: studentInfo?.name || "Kh√¥ng r√µ",
          email: studentInfo?.email || "Kh√¥ng r√µ",
          score: scoreObj.score,
        };
      });

      setStudentsInClass(mergedStudents);
      setSelectedExam(examId);
      setIsModalVisible(true);
    } catch (error) {
      toast.error("Kh√¥ng th·ªÉ t·∫£i b·∫£ng ƒëi·ªÉm.");
      console.error(error);
    }
  };

  const handleDeleteScore = async (examId) => {
    const scoreToDelete = scoreList.find((score) => score.examId === examId);
    if (!scoreToDelete) return;
    try {
      await ScoreService.deleteScore(scoreToDelete._id, token);
      toast.success("Xo√° b·∫£ng ƒëi·ªÉm th√†nh c√¥ng!");
      setScoreList((prev) => prev.filter((score) => score.examId !== examId));
    } catch (error) {
      toast.error("L·ªói khi xo√° b·∫£ng ƒëi·ªÉm.");
      console.error(error);
    }
  };

  const handleCancel = () => {
    setIsModalVisible(false);
    setIsEditMode(false);
    setEditingScoreId(null);
  };

  const handleScoreChange = (e, studentId) => {
    const updatedScore = e?.target?.value ?? e;
    setStudentsInClass((prev) =>
      prev.map((student) =>
        student._id === studentId ? { ...student, score: updatedScore } : student
      )
    );
  };

  const handleSubmitScores = async () => {
    const invalidScores = studentsInClass.filter(
      (student) => student.score === undefined || student.score === null || student.score === ""
    );
    if (invalidScores.length > 0) {
      toast.error("Vui l√≤ng nh·∫≠p ƒëi·ªÉm cho t·∫•t c·∫£ h·ªçc vi√™n.");
      return;
    }

    const scoreData = {
      examId: selectedExam,
      scores: studentsInClass.map((student) => ({
        studentId: student._id,
        score: Number(student.score),
      })),
    };

    try {
      if (isEditMode) {
        await ScoreService.updateScore(editingScoreId, scoreData, token);
        toast.success("C·∫≠p nh·∫≠t b·∫£ng ƒëi·ªÉm th√†nh c√¥ng!");
      } else {
        const response = await ScoreService.createScore(scoreData, token);
        if (
          response.status === "ERROR" &&
          response.message === "B·∫£ng ƒëi·ªÉm cho b√†i thi n√†y ƒë√£ t·ªìn t·∫°i!"
        ) {
          toast.error(response.message);
          return;
        }
        toast.success("ƒêi·ªÉm ƒë√£ ƒë∆∞·ª£c g·ª≠i th√†nh c√¥ng!");
      }

      setIsModalVisible(false);
      setIsEditMode(false);
      setEditingScoreId(null);
      refreshScores();
    } catch (error) {
      console.error("L·ªói khi g·ª≠i/c·∫≠p nh·∫≠t ƒëi·ªÉm:", error);
      toast.error("ƒê√£ x·∫£y ra l·ªói, vui l√≤ng th·ª≠ l·∫°i.");
    }
  };

  const filteredData = studentsInClass.filter((student) =>
    (student?.name || "").toLowerCase().includes(searchText.toLowerCase())
  );

  const columns = [
    {
      title: "STT",
      render: (_, __, index) => index + 1,
      width: 60,
    },
    {
      title: "H·ªç t√™n h·ªçc vi√™n",
      render: (_, record) => record.name || "Kh√¥ng r√µ",
    },
    {
      title: "Email",
      render: (_, record) => record.email || "Kh√¥ng r√µ",
    },
    {
      title: "ƒêi·ªÉm",
      render: (_, record) => (
        <InputNumber
          min={0}
          max={10}
          step={0.1}
          value={record.score || null}
          onChange={(value) => handleScoreChange(value, record._id)}
          style={{ width: 100 }}
        />
      ),
    },
  ];

  return (
    <div style={{ padding: 24 }}>
      <PageHeader>
        <h2>Qu·∫£n l√Ω ƒëi·ªÉm h·ªçc vi√™n</h2>
      </PageHeader>

      <FilterContainer>
        <Select
          placeholder="Ch·ªçn l·ªõp h·ªçc"
          style={{ width: 220 }}
          onChange={setSelectedClass}
          allowClear
        >
          {classList.map((cls) => (
            <Option key={cls._id} value={cls._id}>
              {cls.name}
            </Option>
          ))}
        </Select>
      </FilterContainer>

      <TableWrapper>
        <Table
          dataSource={examList}
          rowKey="_id"
          pagination={false}
          locale={{ emptyText: "Kh√¥ng c√≥ d·ªØ li·ªáu" }}
          columns={[
            {
              title: "STT",
              render: (_, __, index) => index + 1,
              width: 60,
            },
            {
              title: "B√†i thi",
              render: (_, record) => record.examName || "Kh√¥ng r√µ",
            },
            {
              title: "H√†nh ƒë·ªông",
              render: (_, record) => {
                const hasScore = checkScoreExists(record._id);
                if (!selectedClass) {
                  return <span style={{ color: "#888" }}>Ch∆∞a ch·ªçn l·ªõp</span>;
                }
                return hasScore ? (
                  <div style={{ display: "flex", justifyContent: "center", gap: 12 }}>
                    <Tooltip title="S·ª≠a b·∫£ng ƒëi·ªÉm">
                      <Button
                        type="text"
                        icon={<EditOutlined style={{ color: "#1677ff" }} />}
                        onClick={() => handleEditScore(record._id)}
                      />
                    </Tooltip>
                    <Tooltip title="Xo√° b·∫£ng ƒëi·ªÉm">
                      <Button
                        type="text"
                        icon={<DeleteOutlined style={{ color: "red" }} />}
                        onClick={() => handleDeleteScore(record._id)}
                      />
                    </Tooltip>
                  </div>
                ) : (
                  <Button
                    type="primary"
                    icon={<PlusOutlined />}
                    onClick={() => handleExamSelect(record._id)}
                  >
                    T·∫°o b·∫£ng ƒëi·ªÉm
                  </Button>
                );
              },
            },
          ]}
        />
      </TableWrapper>

      <Modal
        title={<h3 style={{ margin: 0, fontWeight: 600 }}>üìã Danh s√°ch h·ªçc vi√™n</h3>}
        open={isModalVisible}
        onCancel={handleCancel}
        footer={null}
        width={900}
        bodyStyle={{ padding: 24 }}
      >
        <div
          style={{
            marginBottom: 16,
            display: "flex",
            justifyContent: "space-between",
          }}
        >
          <Search
            placeholder="üîç T√¨m ki·∫øm h·ªçc vi√™n..."
            onSearch={(value) => setSearchText(value)}
            style={{ width: 300 }}
            allowClear
          />
        </div>

        <Table
          dataSource={filteredData}
          columns={columns}
          rowKey="_id"
          bordered
          pagination={{ pageSize: 8 }}
          locale={{ emptyText: "Ch∆∞a c√≥ d·ªØ li·ªáu h·ªçc vi√™n" }}
        />

        <div style={{ textAlign: "center", marginTop: 30 }}>
          <Button
            type="primary"
            size="large"
            onClick={handleSubmitScores}
            style={{
              borderRadius: 8,
              padding: "6px 24px",
              fontWeight: 600,
              boxShadow: "0px 4px 10px rgba(0, 0, 0, 0.1)",
            }}
          >
            {isEditMode ? "üíæ L∆∞u ch·ªânh s·ª≠a" : "üöÄ G·ª≠i ƒëi·ªÉm"}
          </Button>
        </div>
      </Modal>
    </div>
  );
};

export default ScoreManagement;
